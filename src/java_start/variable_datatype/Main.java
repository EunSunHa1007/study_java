package java_start.variable_datatype;

public class Main {
    public static void main(String[] args) {
        //대입(할당)연산자 assignment operator - 선언과 초기화를 동시에
        int age = 20;
        boolean isMarried = false;
        double height = 161;
        String name = "내이름";
        // 변수가 아닌 = 오른쪽의 값들을 리터럴 literal 이라 부른다.

        //쉼표 사용하여 여러 변수를 한 줄에 선언 및 초기화 가능.
        char ch1, ch2, ch3; //선언만
        char ch4 = 'A', ch5 = 'B', ch6 = 'C'; //초기화까지

    }
}
